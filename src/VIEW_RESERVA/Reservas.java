    private void listarValores(){
        try{
            ReservaDAO reservaDAO = new ReservaDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabelaReservas.getModel();
            model.setNumRows(0);
            
            ArrayList<ReservaDTO> lista = reservaDAO.ListarReservas();
            
            for (int num = 0; num < lista.size(); num++){
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getId_cliente(),
                    lista.get(num).getNome(),
                    lista.get(num).getNumero_quarto(),
                    lista.get(num).getDataIN(),
                    lista.get(num).getDataOUT()
                });
            }
        } catch (Exception erro){
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW: " + erro);
        }
    }
    
