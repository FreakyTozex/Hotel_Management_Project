    private void btnEliminarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarReservaActionPerformed
        // TODO add your handling code here:
        try{
            int id_reserva = Integer.parseInt(txtIDReserva.getText());
            int nr_quarto=Integer.parseInt(txtNQuarto.getText());
            int nif = Integer.parseInt(txtNIF.getText());
            ReservaDTO reservadto = new ReservaDTO();
            reservadto.setId(id_reserva);
            reservadto.setNumero_quarto(nr_quarto);
            reservadto.setId_cliente(nif);
            
            ReservaDAO reservadao = new ReservaDAO();
            reservadao.eliminarReserva(reservadto);

            
        } catch (Exception erro){
            JOptionPane.showMessageDialog(null, "FRM_INSERIR_RESERVA_VIEW " + erro);
        }
        
    }//GEN-LAST:event_btnEliminarReservaActionPerformed

    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        // TODO add your handling code here:
        try{
            int NIF, nr_quarto;
            SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");           
            String checkin = dateformat.format(dateIN.getDate());  
            String checkout = dateformat.format(dateOUT.getDate()); 
            NIF = Integer.parseInt(txtNIF.getText());
            nr_quarto = Integer.parseInt(txtNQuarto.getText());
            ReservaDTO reservadto = new ReservaDTO();
             
            reservadto.setId_cliente(NIF);
            reservadto.setNumero_quarto(nr_quarto);
            reservadto.setDataIN(checkin);
            reservadto.setDataOUT(checkout);
            
            ReservaDAO reservadao = new ReservaDAO();
            reservadao.inserirReserva(reservadto);

            
        } catch (Exception erro){
            JOptionPane.showMessageDialog(null, "FRM_INSERIR_RESERVA_VIEW " + erro);
        }
        
    }//GEN-LAST:event_btnReservarActionPerformed

